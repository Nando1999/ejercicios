
# Ingresar la longitud y el ancho de un rectángulo y encontrar su perímetro.
longuitud = int(input("Ingrese la longuitud ")) # pide al usurio ingresar longuitud 
ancho = int (input("ingrese la ancho")) #pide al usuario ingresar el ancho 
perimetro = 2* (ancho + longuitud)  # formula para calcular el  perimetro del rectangulo 
area = (ancho * longuitud)   # formula para calcular el area del rectangulo 
print (f"El perímetro es : {perimetro}")  #imprime perimetro 
print (f"El área es :{area}") #imprime area 
